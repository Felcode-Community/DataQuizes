# function to find area of circle
def areaOfCircle():
    pi = 3.142

    raduis = 14

    area = pi * raduis ** 2

    print(f"Area of the circle {area} m2")


# function to find the perimeter of a circle
def perimeter():
    pi = 22/7
    diameter = 28

    perimeter_of_circle = pi * diameter

    print(f"Perimeter is {perimeter_of_circle} m")


areaOfCircle()
perimeter()